    private void weightButtonActionPerformed(int buttonNumer) {
        
        if(weightPanels[buttonNumer].isVisible()) {
            weightPanels[buttonNumer].setVisible(false);
            currentWeight= currentWeight- weights[buttonNumer]; 
            currentValue = currentValue -values[buttonNumer]; 
            setCurrentValuesText();
            emptyPanelLength = emptyPanelLength + heights[buttonNumer];
            emptyPanel.setSize(120,emptyPanelLength);
            emptyPanel.setMaximumSize(new Dimension(120,emptyPanelLength));

        }else {
                
            if(currentWeightCheck(currentWeight+ weights[buttonNumer])) {
                JOptionPane.showMessageDialog(this, "You can only add upto  " + (maxWeight - currentWeight) + " weight");
            } else {
            weightPanels[buttonNumer].setVisible(true);
            weightPanels[buttonNumer].setSize(120,heights[buttonNumer]);
            weightPanels[buttonNumer].setMaximumSize(new Dimension(120,heights[buttonNumer]));
            emptyPanelLength = emptyPanelLength - heights[buttonNumer];
            emptyPanel.setSize(120,emptyPanelLength);
            emptyPanel.setMaximumSize(new Dimension(120,emptyPanelLength));
            currentWeight= currentWeight+ weights[buttonNumer]; 
            currentValue = currentValue +values[buttonNumer]; 
            setCurrentValuesText();

                
            }
        }
    }


