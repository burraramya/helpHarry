    private void solveKnapSack() {

        SolveKnapSack sol = new SolveKnapSack(this.maxWeight, this.weights, this.values, 10);

        int counter = 0;
        for(int wght : sol.getSolvedWeights()) {

            if(wght == -1) {
                if(counter == 0 ) {
                    System.out.println("No Solution");
                }
                break;
            }
            counter++;

            for(int i =0; i<10; i++) {
                if(this.weights[i] == wght) {
                    weightButtonActionPerformed(i);
                    break;
                }
            }
        }

    }

